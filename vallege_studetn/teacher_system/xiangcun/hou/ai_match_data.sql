USE rural_education_platform;

-- ============================================
-- 表1: AI教师档案表 (学生端匹配时读取)
-- ============================================
CREATE TABLE IF NOT EXISTS ai_teacher_profiles (
    id INT PRIMARY KEY AUTO_INCREMENT,
    
    -- 基本信息
    name VARCHAR(50) NOT NULL COMMENT '教师姓名',
    subject VARCHAR(50) NOT NULL COMMENT '教授科目',
    education VARCHAR(50) COMMENT '教育背景',
    experience VARCHAR(50) COMMENT '教学经验',
    rating DECIMAL(3,2) DEFAULT 4.0 COMMENT '教师评分(0-5)',
    
    -- AI匹配关键字段
    introduction TEXT COMMENT '个人简介(用于性格匹配)',
    teaching_style VARCHAR(100) COMMENT '教学风格关键词',
    specialties TEXT COMMENT '专长领域',
    
    -- 可辅导信息
    available_grades VARCHAR(100) COMMENT '可辅导年级范围',
    max_students INT DEFAULT 10 COMMENT '最大学生数',
    
    -- 元数据
    is_active BOOLEAN DEFAULT TRUE COMMENT '是否活跃',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    INDEX idx_subject (subject),
    INDEX idx_rating (rating),
    INDEX idx_active (is_active)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='AI匹配专用教师档案表';

-- ============================================
-- 表2: AI学生档案表 (教师端匹配时读取)
-- ============================================
CREATE TABLE IF NOT EXISTS ai_student_profiles (
    id INT PRIMARY KEY AUTO_INCREMENT,
    
    -- 基本信息
    name VARCHAR(50) NOT NULL COMMENT '学生姓名',
    grade VARCHAR(20) NOT NULL COMMENT '年级',
    school VARCHAR(100) COMMENT '学校',
    
    -- AI匹配关键字段
    learning_needs TEXT COMMENT '学习需求描述',
    weak_subjects VARCHAR(100) COMMENT '薄弱科目',
    learning_goals TEXT COMMENT '学习目标',
    
    -- 性格与偏好
    preferred_teacher_personality VARCHAR(100) COMMENT '期望教师性格',
    learning_style VARCHAR(50) COMMENT '学习风格',
    
    -- 元数据
    is_active BOOLEAN DEFAULT TRUE COMMENT '是否活跃',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    INDEX idx_grade (grade),
    INDEX idx_active (is_active)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='AI匹配专用学生档案表';

